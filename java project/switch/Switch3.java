class Switch3
{
  public static void main(String[] args) 
    {
      char grade='C';
      if(grade=='A')
       {
        System.out.println("FCD");
       }
        else if(grade=='B')
         {
           System.out.println("FC");
         }
        else if(grade=='C')
         {                 
           System.out.println("SC");
         }
        else if(grade=='D')
         {
           System.out.println("JUST PASS");
         }
        else if(grade=='E')
         {
           System.out.println("fail");
         }
        else
         {
           System.out.println("invalid grade");
        }
    }
}